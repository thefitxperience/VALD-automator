def get_branches(gym: str) -> list[str]:
    return list(TRAINERS.get(gym, {}).keys())


def get_trainers(gym: str, branch: str) -> list[dict]:
    return TRAINERS.get(gym, {}).get(branch, {}).get("trainers", [])


def get_trainer_whatsapp(gym: str, branch: str, trainer_name: str) -> str:
    for t in get_trainers(gym, branch):
        if t["name"] == trainer_name:
            return t.get("whatsapp", "")
    return ""
